def celsius_to_fahrenheit(c):
    return (c * (9 / 5)) + 32


def fahrenheit_to_celsius(f):
    return (f - 32) * (5 / 9)


def mm_to_inches(mm):
    return mm / 25.4


def inches_to_mm(i):
    return i * 25.4


def kmh_to_mph(kmh):
    return kmh / 1.609


def mph_to_kmh(mph):
    return mph * 1.609
